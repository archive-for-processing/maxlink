<p class="summary">
The MaxLink java libraries enable communication between 
<a class="sitelink" href="http://processing.org">Processing</a>
 and 
 <a class="sitelink" href="http://cycling74.com">Max/MSP 4.5</a> on both Mac and Windows.
</p>

<p>
Version 0.33 is available for <a href="?page=downloads">download</a>.  It provides basic support for sending and receiving messages between Processing and Max, a Max object for launching Processing sketches, and some tools for creative networking. 
</p>

<p>
MaxLink works by multicasting UDP messages over the local subnet.  It uses a specific namespace protocol to match the inlets and outlets of Max objects with declared inputs and outputs of a Java processes (including Processing sketches).
</p>

<p>
Several simple examples are included in the download package.  It only takes a few lines of code to get your Processing sketch talking to Max.
</p>

